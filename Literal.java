public class Literal {
    // Literals are values assigned to a variable
    public static void main(String[] args){
        int num1 = 0b101;
        int n = 10_00_000_00;
        double d = 12e10;
        char c ='a';
        c++;
        System.out.println(num1);
        System.out.println(n);
        System.out.println(d);
        System.out.println(c);
    }
}
